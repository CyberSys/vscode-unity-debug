namespace UnityDebug
{
	public class Varible
	{
		public string name;
		public string value;
		public int variablesReference;

		public Varible (string name, string value, int variablesReference)
		{
			this.name = name;
			this.value = value;
			this.variablesReference = variablesReference;
		}
	}
}

